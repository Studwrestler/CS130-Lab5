package lab5;
enum Days {Monday,Tuesday,Wednesday,Thursday,Friday,Saturday,Sunday}
public class Enum_Days {

	public static void main(String[] args) {
		Days d1 = Days.Monday;
		System.out.println(d1);
		System.out.println(d1.ordinal());
		Days d2 = Days.Tuesday;
		System.out.println(d2);
		System.out.println(d2.ordinal());
		Days d3 = Days.Wednesday;
		System.out.println(d3);
		System.out.println(d3.ordinal());
		Days d4 = Days.Thursday;
		System.out.println(d4);
		System.out.println(d4.ordinal());
		Days d5 = Days.Friday;
		System.out.println(d5);
		System.out.println(d5.ordinal());
		Days d6 = Days.Saturday;
		System.out.println(d6);
		System.out.println(d6.ordinal());
		Days d7 = Days.Sunday;
		System.out.println(d7);
		System.out.println(d7.ordinal());
		
	}

}
